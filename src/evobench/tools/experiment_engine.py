import numpy as np
import json
import time
import platform
from datetime import datetime
from evobench.benchmarks import get_benchmark
from typing import List, Dict, Any

def get_evaluated_benchmarks(json_path: str) -> list:
    """
    Scans the experiment JSON file to discover all evaluated benchmark functions.
    
    Args:
        json_path (str): The file path to the saved JSON experiment results.
        
    Returns:
        list: A collection of strings representing the names of the test functions.
    """
    with open(json_path, 'r') as f:
        data = json.load(f)
        
    # Validation to ensure the file follows the expected schema
    if "results" not in data:
        raise KeyError("The provided JSON does not contain a 'results' block.")
        
    # Extract the keys from the results dictionary, which correspond to the benchmarks
    benchmark_names = list(data["results"].keys())
    
    return benchmark_names

def unpack_fitness_results(json_path: str, target_function: str) -> Dict[str, np.ndarray]:
    """
    Extracts and organizes fitness vectors for a specific benchmark function from experimental results.
    
    This function reads the JSON file generated by the experiment engine and reconstructs
    the fitness vectors for each algorithm that was tested on the specified benchmark function.
    The fitness values are returned as numpy arrays for convenient statistical analysis.

    Args:
        json_path (str): The file path to the JSON results file.
        target_function (str): The name of the benchmark function (e.g., 'Ackley', 'Sphere').

    Returns:
        Dict[str, np.ndarray]: A dictionary mapping algorithm names to their fitness vectors,
                               where each vector contains the best_fitness values from all runs.
    """
    # Open and parse the JSON results file containing all experimental data
    with open(json_path, 'r') as file:
        data = json.load(file)
    
    # Navigate to the results block for the specified benchmark function
    function_results = data["results"][target_function]
    
    # Initialize a dictionary to store unpacked fitness data for each algorithm
    unpacked_data = {}
    
    # Iterate through each algorithm and its corresponding list of runs
    for algorithm_name, runs_list in function_results.items():
        # Extract only the best_fitness value from each independent run
        # This creates a vector of fitness values (best result per run) for statistical analysis
        fitness_vector = [run["best_fitness"] for run in runs_list]
        
        # Store the fitness vector as a numpy array for convenient mathematical operations
        unpacked_data[algorithm_name] = np.array(fitness_vector)
        
    return unpacked_data

def run_automated_experiment(config: Dict[str, Any], output_file: str = "results.json") -> None:
    """
    Orchestrates the execution of multiple metaheuristics across various benchmark functions.
    
    This function manages a complete experimental workflow: it instantiates algorithms,
    executes them on benchmark functions, collects performance metrics, and saves
    comprehensive results to a JSON file. The function performs multiple independent
    runs per algorithm-benchmark combination to enable robust statistical analysis.
    
    The experimental workflow:
    - Captures system metadata and experiment timestamps
    - Initializes a structured report for results storage
    - Iterates through all benchmark functions in the configuration
    - For each benchmark, executes all specified algorithms
    - Performs independent runs with timing and convergence tracking
    - Saves the complete experimental report to JSON format

    Args:
        config (Dict[str, Any]): Configuration dictionary containing:
            - dimensions (int): Dimensionality of the optimization problems
            - max_iterations (int): Number of generations per algorithm run
            - population_size (int): Population size for each algorithm
            - independent_runs (int): Number of independent trials per algorithm-benchmark pair
            - benchmarks (List[Dict]): List of benchmark function specifications
            - algorithms (List[Dict]): List of algorithm specifications with parameters
        output_file (str): Path where the JSON results file will be saved. Defaults to "results.json".
    """
    # Capture the current date and time for experiment metadata documentation
    start_time_stamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    # Initialize the experiment report structure with metadata headers
    # This will contain comprehensive information about the experimental session
    experiment_report = {
        "experiment_info": {
            "date": start_time_stamp,
            "system": f"{platform.system()} {platform.release()}",
            "evobench_version": "0.1.0"
        },
        "global_configuration": {
            "dimensions": config["dimensions"],
            "max_iterations": config["max_iterations"],
            "population_size": config["population_size"],
            "independent_runs": config["independent_runs"]
        },
        "algorithms_config": {},
        "results": {}
    }

    # Extract frequently accessed configuration parameters for convenience
    # This reduces dictionary lookups and improves code readability
    dimensions = config["dimensions"]
    independent_runs = config["independent_runs"]
    population_size = config["population_size"]
    max_iterations = config["max_iterations"]

    # Iterate through each benchmark function specified in the configuration
    for benchmark in config["benchmarks"]:
        # Retrieve benchmark function metadata and references
        function_name = benchmark["name"]
        function_reference = get_benchmark(function_name)
        search_space_bounds = np.array(benchmark["bounds"])
        
        print(f"\n>>> Evaluating Benchmark: {function_name}")
        experiment_report["results"][function_name] = {}

        # Iterate through each algorithm to be evaluated on this benchmark
        for algorithm_info in config["algorithms"]:
            # Extract algorithm specification and hyperparameters
            algorithm_name = algorithm_info["name"]
            algorithm_class = algorithm_info["class"]
            algorithm_parameters = algorithm_info["params"]
            
            # Save algorithm configuration metadata once to avoid redundant storage
            # This information is useful for result interpretation and reproducibility
            if algorithm_name not in experiment_report["algorithms_config"]:
                experiment_report["algorithms_config"][algorithm_name] = {
                    "class": algorithm_class.__name__,
                    "params": algorithm_parameters
                }

            # Provide user feedback on the current execution progress
            print(f"    Running {algorithm_name}...", end=" ", flush=True)
            experiment_report["results"][function_name][algorithm_name] = []

            # Execute multiple independent runs for statistical validity and robustness
            for run_index in range(independent_runs):
                # Instantiate a fresh algorithm instance with the configured hyperparameters
                optimizer_instance = algorithm_class(
                    objective_function=function_reference,
                    bounds=search_space_bounds,
                    population_size=population_size,
                    max_iterations=max_iterations,
                    **algorithm_parameters
                )
                
                # Measure wall-clock execution time for performance profiling
                execution_start_time = time.perf_counter()
                best_position, best_fitness = optimizer_instance.run()
                execution_end_time = time.perf_counter()
                
                # Organize run results with explicit type conversions for JSON serialization
                # NumPy types must be converted to native Python types for JSON compatibility
                run_results = {
                    "run": run_index + 1,
                    "best_fitness": float(best_fitness),
                    "best_position": best_position.tolist(),
                    "execution_time_sec": execution_end_time - execution_start_time,
                    "convergence_history": [float(fitness_value) for fitness_value in optimizer_instance.fitness_history]
                }
                
                # Append this run's results to the algorithm's result collection
                experiment_report["results"][function_name][algorithm_name].append(run_results)
            
            # Indicate successful completion of this algorithm
            print("Done.")

    # Persist the entire experimental report to a JSON file for archival and analysis
    with open(output_file, "w") as output_stream:
        json.dump(experiment_report, output_stream, indent=4)
    
    # Provide confirmation and location information to the user
    print(f"\n{'='*40}\nExperiment Complete. Saved to: {output_file}\n{'='*40}")